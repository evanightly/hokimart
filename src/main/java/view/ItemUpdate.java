    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        try {
            String title = this.title.getText();
            String description = this.description.getText();
            int id_category = categorySourceData.get(this.categories.getSelectedIndex()).getId_category();
            float price = Float.parseFloat(this.price.getValue().toString());
            int in_stock = Integer.parseInt(this.stock.getValue().toString());
            System.out.println(title);
            System.out.println(description);
            System.out.println(id_category);
            System.out.println(price);
            System.out.println(in_stock);
            ItemController.update(id_item, title, description, id_category, price, in_stock);
            this.dispose();
        } catch (SQLException ex) {
            Logger.getLogger(ItemUpdate.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_updateActionPerformed

