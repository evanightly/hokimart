    public ItemUpdate(java.awt.Frame parent, boolean modal, String selectedCategory, int id_item) {
        super(parent, modal);
        this.selectedCategory = selectedCategory;
        this.id_item = id_item;
        try {
            categorySourceData = CategoryController.get();
            ArrayList<String> categoryStrings = new ArrayList<>();
            for (Category category : categorySourceData) {
                categoryStrings.add(category.getTitle());
            }
            initComponents();
            DefaultComboBoxModel model = new DefaultComboBoxModel<>(categoryStrings.toArray());
            int selectedCategoryIndex = categoryStrings.indexOf(selectedCategory);
            categories.setModel(model);
            categories.setSelectedIndex(selectedCategoryIndex);
        } catch (SQLException ex) {
            Logger.getLogger(ItemUpdate.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

